function out = PoH20_T(T)
% T in Kelvins
if T <=303
    out = 10.^(5.40221 - 1838.675./(T-31.737));
elseif T>303 && T<=333
    out = 10^(5.20389 - 1733.926/(T-39.485));
elseif T>333 && T<=363
    out = 10^(5.0768 - 1659.793/(T-45.854));
elseif T>364 && T<=373
    out = 10^(5.08354 - 1663.125/(T-45.622));
else 
    out = 10^(3.55959 - 643.748/(T-198.043));
end
out = out *10^5;
end